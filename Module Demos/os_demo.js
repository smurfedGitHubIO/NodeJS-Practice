const os = require('os');

// Prints home directory
console.log(os.homedir());

// Prints the OS Type which is Windows NT
console.log(os.type());

// Prints the free memory of my laptop
console.log(os.freemem());

// Prints the total memory of my laptop
console.log(os.totalmem());

// Prints the uptime of laptop in seconds
console.log(os.uptime());

